import java.util.*;

public class Basics {
    public static void main(String args[]){
        System.out.println("hello world");

        // Print pattern
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");

        // Variables & Literals
        int a= 10;
        int b = 8;
        System.out.println(a+" "+b);
        a = 45;
        System.out.println(a + " " + b);
        String at = "ayush";
        System.out.println(at);
    }
}